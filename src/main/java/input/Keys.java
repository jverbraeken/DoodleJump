package input;

/**
 * Public enum to identify keys.
 */
public enum Keys {

    // Arrows
    /**
     * The left arrow key.
     */
    arrowLeft,
    /**
     * The Right arrow key.
     */
    arrowRight,

    // Letters
    /**
     * The A key.
     */
    a,
    /**
     * The D key.
     */
    d,

    // Miscellaneous
    /**
     * The Enter key.
     */
    enter,
    /**
     * The Space key.
     */
    space

}
